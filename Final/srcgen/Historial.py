class Historial:
  def __init__(this,codigo,historial):
    this.codigo=codigo
    this.historial=historial

  def getCodigo():
    return this.codigo

  def getHistorial():
    return this.historial

  def setCodigo(this,codigo):
    this.codigo = codigo

  def setHistorial(this,historial):
    this.historial = historial

